<h3>participate</h3>
<div>
	<em>cosmicplayground</em>
	(CPG) hopes to bring together likeminded explorers sharing ideas and
	experiences. Participation can take many forms:
</div>
<ul>
	<li>
		report problems, ask questions, give feedback, and discuss ideas in
		<a href="https://github.com/ryanatkn/cosmicplayground/issues">
			the GitHub issues
		</a>
	</li>
	<li>
		join the subreddit at
		<a href="https://reddit.com/r/cosmicplayground">/r/cosmicplayground</a>
	</li>
	<li>
		tweet
		<a href="https://twitter.com/cpg_org">@cpg_org</a>
	</li>
	<li>
		say hi on
		<a href="https://discord.gg/57XP5Pv">Discord</a>
	</li>
	<li>
		contribute code and other assets like art and audio. Please be aware that
		your submissions will be
		<a href="https://github.com/ryanatkn/cosmicplayground/blob/master/LICENSE">
			CC0 licensed
		</a>
		(public domain) unless you explicitly request otherwise. Code is an
		exception - all of it is CC0. See
		<a
			href="https://github.com/ryanatkn/cosmicplayground/blob/master/docs/credits.md">
			the credits
		</a>
		and
		<a
			href="https://github.com/ryanatkn/cosmicplayground/blob/master/docs/contributors.md">
			contributors
		</a>
		for more. Also, if you want to make sure I'll accept your stuff, please open
		an issue so we can talk about it first.
	</li>
	<li>
		email
		<a href="mailto:cosmicplayground.org@gmail.com">
			cosmicplayground.org@gmail.com
		</a>
	</li>
	<li>
		donate on
		<a href="https://patreon.com/ryanatkn">Patreon</a>
	</li>
	<li>
		fork the source code on
		<a href="https://github.com/ryanatkn/cosmicplayground/issues">GitHub</a>
		- it's easy to self-host on a cheap server
	</li>
</ul>
